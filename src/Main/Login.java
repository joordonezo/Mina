    private void ingresar() {
        try {
            char[] cla = claveTxt.getPassword();
            String clave = new String(cla);
            if (usuarioTxt.getText() != "" && clave != "") {
                String usu = usuarioTxt.getText();
                String claveMd5 = clave;
                //System.out.println(clave);
                ArrayList<String> datos = Persona.consultarIdPersonaLogin(usu, claveMd5);
                this.setVisible(false);
                Inicio main = new Inicio(Integer.parseInt(datos.get(0)), datos.get(1));
                main.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Acceso incorrecto");
            }
        } catch (Exception e) {
        }
    }

