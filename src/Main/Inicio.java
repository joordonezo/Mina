    private static int filPorMina() {
        ArrayList<String> minas = (ArrayList<String>) Mina.consultarMinas();

        String[] vecMinas = new String[(minas.size() / 2) + 2];

        int i = 0;
        int a = 0, b = 1;
        for (i = 0; i < vecMinas.length - 2; i++) {

            vecMinas[i] = minas.get(a) + " - " + minas.get(b);
            a += 2;
            b += 2;
        }

        vecMinas[i] = "Todas";
        vecMinas[i + 1] = "Seleccione";

        String opcion = (String) JOptionPane.showInputDialog(null, "Filtrar por mina", "Elegir", JOptionPane.QUESTION_MESSAGE, null, vecMinas, vecMinas[vecMinas.length - 1]);
        int idMina = 0;
        if (opcion.startsWith("Sel")) {
            JOptionPane.showMessageDialog(null, "Selecione una mina");
        } else if (opcion.startsWith("Tod")) {
            return idMina;
        } else {
            idMina = Integer.parseInt(opcion.substring(0, 1));
            //System.out.println(idMina);
        }
        return idMina;
    }

